package ru.stairenx.nvsutimetable.item;

public class Teacheritem {

    String name;
    String staffer;

    public Teacheritem(String name, String staffer) {
        this.name = name;
        this.staffer = staffer;
    }

    public String getName() {
        return name;
    }

    public String getStaff() {
        return staffer;
    }
}
